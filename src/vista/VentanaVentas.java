private void configurarTablas() {
    DefaultTableModel modelCatalogo = new DefaultTableModel();
    modelCatalogo.addColumn("ID");
    modelCatalogo.addColumn("Nombre");
    modelCatalogo.addColumn("Tipo");
    modelCatalogo.addColumn("Precio");
    modelCatalogo.addColumn("Stock");
    tablaCatalogo.setModel(modelCatalogo);

    DefaultTableModel modelCarrito = new DefaultTableModel();
    modelCarrito.addColumn("Nombre");
    modelCarrito.addColumn("Cantidad");
    modelCarrito.addColumn("Precio Unit.");
    modelCarrito.addColumn("Subtotal");
    tablaCarrito.setModel(modelCarrito);
}

private void cargarCatalogo() {
    DefaultTableModel model = (DefaultTableModel) tablaCatalogo.getModel();
    model.setRowCount(0);
    List<Producto> lista = productoDAO.obtenerTodos();
    for (Producto p : lista) {
        if (p.getCantidadActual() > 0) {
            model.addRow(new Object[]{
                p.getIdProducto(),
                p.getNombre(),
                p.getTipoProducto(),
                p.getPrecioVenta(),
                p.getCantidadActual()
            });
        }
    }
}

private void actualizarTotal() {
    double total = 0;
    for (LineaDeVenta linea : carrito) {
        total += linea.getSubtotal();
    }
    lblTotal.setText("Total: $" + String.format("%.2f", total * 1.19));
} 
    
