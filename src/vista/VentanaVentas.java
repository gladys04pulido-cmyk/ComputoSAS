    private void btnAgregarCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCarritoActionPerformed
    int fila = tablaCatalogo.getSelectedRow();
    if (fila == -1) {
      javax.swing.JOptionPane.showMessageDialog(this,
        "Seleccione un producto del catálogo", "Error",
        javax.swing.JOptionPane.ERROR_MESSAGE);
      return;
    }

    String cantidadStr = javax.swing.JOptionPane.showInputDialog(this, 
     "¿Cuántas unidades desea agregar?");
    if (cantidadStr == null) return;

   try {
    int cantidad = Integer.parseInt(cantidadStr);
    int idProducto = (int) tablaCatalogo.getValueAt(fila, 0);
    Producto p = productoDAO.buscarPorId(idProducto);

    if (cantidad > p.getCantidadActual()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "No hay suficiente stock", "Error",
            javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    LineaDeVenta linea = new LineaDeVenta(carrito.size() + 1, p, cantidad);
    carrito.add(linea);

    DefaultTableModel modelCarrito = (DefaultTableModel) tablaCarrito.getModel();
    modelCarrito.addRow(new Object[]{
        p.getNombre(),
        cantidad,
        p.getPrecioVenta(),
        linea.getSubtotal()
    });
    actualizarTotal();

   } catch (NumberFormatException e) {
      javax.swing.JOptionPane.showMessageDialog(this,
        "Ingrese un número válido", "Error",
        javax.swing.JOptionPane.ERROR_MESSAGE);
     }
    }//GEN-LAST:event_btnAgregarCarritoActionPerformed

    private void btnProcesarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarVentaActionPerformed
    if (carrito.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "El carrito está vacío", "Error",
        javax.swing.JOptionPane.ERROR_MESSAGE);
    return;
}

    String nombreCliente = txtNombreCliente.getText();
    if (nombreCliente.isEmpty()) {
      javax.swing.JOptionPane.showMessageDialog(this,
        "Ingrese el nombre del cliente", "Error",
        javax.swing.JOptionPane.ERROR_MESSAGE);
      return;
    }

  try {
    // Buscar cliente existente o crear uno nuevo
    List<Cliente> clientes = clienteDAO.obtenerTodos();
    int idCliente = -1;
    for (Cliente c : clientes) {
      if (c.getNombre().equalsIgnoreCase(nombreCliente)) {
        idCliente = c.getIdCliente();
        break;
    }
  }
    if (idCliente == -1) {
       Cliente cliente = new Cliente(0, nombreCliente, "", "", "");
       clienteDAO.agregar(cliente);
       clientes = clienteDAO.obtenerTodos();
       idCliente = clientes.get(clientes.size() - 1).getIdCliente();
    }
    // Crear método de pago
    String metodoPago = cmbMetodoPago.getSelectedItem().toString();
    double total = 0;
    for (LineaDeVenta linea : carrito) {
        total += linea.getSubtotal();
    }
    total = total * 1.19;

    MetodoPago pago = null;
    switch (metodoPago) {
        case "Efectivo":
            pago = new Efectivo(total);
            break;
        case "Tarjeta Débito":
            String numDebito = javax.swing.JOptionPane.showInputDialog(
                this, "Ingrese número de tarjeta:");
            pago = new TarjetaDebito(total, numDebito, nombreCliente);
            break;
        case "Tarjeta Crédito":
            String numCredito = javax.swing.JOptionPane.showInputDialog(
                this, "Ingrese número de tarjeta:");
            String cuotasStr = javax.swing.JOptionPane.showInputDialog(
                this, "Ingrese número de cuotas:");
            int cuotas = Integer.parseInt(cuotasStr);
            pago = new TarjetaCredito(total, numCredito, nombreCliente, cuotas);
            break;
    }

    // Procesar pago
    if (pago != null && pago.procesarPago()) {
        int idMetodoPago = metodoPagoDAO.agregar(pago);

        // Crear venta
        String folio = Venta.generarFolio();
        Venta venta = new Venta(folio, idCliente, pago);
        for (LineaDeVenta linea : carrito) {
            venta.agregarLinea(linea);
        }

        // Verificar stock
        boolean hayPendientes = false;
        for (LineaDeVenta linea : carrito) {
            if (linea.getProducto().isEsBajoPedido()) {
                hayPendientes = true;
                break;
            }
        }

        venta.setEstatusVenta(hayPendientes ? "PENDIENTE" : "ENTREGADA");
        int idVenta = ventaDAO.agregar(venta, idMetodoPago);
        ventaDAO.agregarLineas(idVenta, carrito);

        // Mostrar documentos
        String documentos = venta.generarFactura() + "\n\n" + 
                           venta.generarGarantia() + "\n\n" + 
                           venta.generarManifiesto();

        javax.swing.JTextArea textArea = new javax.swing.JTextArea(documentos);
        textArea.setEditable(false);
        javax.swing.JScrollPane scrollPane = new javax.swing.JScrollPane(textArea);
        scrollPane.setPreferredSize(new java.awt.Dimension(600, 400));
        javax.swing.JOptionPane.showMessageDialog(this, scrollPane,
            "Documentos de Venta - Folio: " + folio,
            javax.swing.JOptionPane.INFORMATION_MESSAGE);

        // Limpiar carrito
        carrito.clear();
        ((DefaultTableModel) tablaCarrito.getModel()).setRowCount(0);
        lblTotal.setText("Total: $0.0");
        txtNombreCliente.setText("");
        cargarCatalogo();
    }

   } catch (Exception e) {
      javax.swing.JOptionPane.showMessageDialog(this,
        "Error al procesar venta: " + e.getMessage(), "Error",
        javax.swing.JOptionPane.ERROR_MESSAGE);
     }
    }//GEN-LAST:event_btnProcesarVentaActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
    String busqueda = txtBuscarProducto.getText().toLowerCase();
    DefaultTableModel model = (DefaultTableModel) tablaCatalogo.getModel();
    model.setRowCount(0);
    List<Producto> lista = productoDAO.obtenerTodos();
    for (Producto p : lista) {
      if (p.getNombre().toLowerCase().contains(busqueda) && p.getCantidadActual() > 0) {
        model.addRow(new Object[]{
            p.getIdProducto(),
            p.getNombre(),
            p.getTipoProducto(),
            p.getPrecioVenta(),
            p.getCantidadActual()
         });
       }
     }
    }//GEN-LAST:event_btnBuscarActionPerformed

